<?php

class Tarefa{
  $data;
  $horaInicio;
  $horaFim;
  $nomeTarefa;
  $foiConcluida;
  $descricao;
  function __construct($data, $horaInicio, $horaFim, $nomeTarefa, $foiConcluida, $descricao) {
    $this->data = $data
    $this->horaInicio = $horaInicio
    $this->nomeTarefa = $nomeTarefa
    $this->foiConcluida = $foiConcluida
    $this->descricao = $descricao
  }
}

